public class Arithmetic {
    int add(int a,int b)
    {
        int c;
        c =a+b;
        return c;

    }
    int mul(int a,int b)
    {
        int c;
        c =a*b;
        return c;

    }
    float div(int a,int b)
    {
        float c;
        c =a/b;
        return c;

    }
    int sub(int a,int b)
    {
        int c;
        c =a-b;
        return c;

    }

}
